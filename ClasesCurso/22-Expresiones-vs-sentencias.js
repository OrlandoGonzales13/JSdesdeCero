// Expresiones vs Sentencias
// En JavaScript, una sentencia es una instrucción que realiza una acción. 
// Por ejemplo, una sentencia podría asignar un valor a una variable,
//  ejecutar una función, o cambiar el flujo de ejecución de un programa a través de una sentencia if o 
//  un bucle for. Aquí tienes algunos ejemplos de sentencias:


let x = 5; // Esta es una sentencia de asignación

function saludo() { // Esta es una sentencia de función
    console.log("¡Hola mundo!");
}

if (x > 0) { // Esta es una sentencia if
    console.log("x es positivo");
}

// Por otro lado, una expresión es cualquier código que produce un valor. 
// Las expresiones pueden ser tan simples como un número o una cadena de texto, o 
// tan complejas como una llamada a una función que devuelve un valor. 
// Aquí tienes algunos ejemplos de expresiones:


5 // Esto es una expresión

"x" // Esto también es una expresión

2 + 2 // Esto es una expresión que suma dos números

saludo() // Esto es una expresión de llamada a función
// 💡 En resumen, las sentencias realizan acciones y las expresiones producen valores.Un programa en JavaScript está compuesto por una serie de sentencias, y estas sentencias pueden contener expresiones.



